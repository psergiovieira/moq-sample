using System.Text.RegularExpressions;

namespace Infrastructure.Regex
{
    public static class RegexHelper
    {
        /// <summary>
        /// Taken from http://haacked.com/archive/2007/08/21/i-knew-how-to-validate-an-email-address-until-i.aspx
        /// </summary>
        /// <returns></returns>
        public static System.Text.RegularExpressions.Regex CreateValidEmailRegex()
        {
            string validEmailPattern = @"^(?!\.)(""([^""\r\\]|\\[""\r\\])*""|"
                + @"([-a-z0-9!#$%&'*+/=?^_`{|}~]|(?<!\.)\.)*)(?<!\.)"
                + @"@[a-z0-9][\w\.-]*[a-z0-9]\.[a-z][a-z\.]*[a-z]$";

            return new System.Text.RegularExpressions.Regex(validEmailPattern, RegexOptions.IgnoreCase);
        }
    }
}
